-- 푸드다이어리 데이터베이스 스키마
DROP TABLE hk_user_foods CASCADE CONSTRAINTS;
DROP TABLE hk_user_favorite_foods CASCADE CONSTRAINTS;
DROP TABLE hk_food_reference_weights CASCADE CONSTRAINTS;
DROP TABLE hk_food_types CASCADE CONSTRAINTS;
DROP TABLE hk_user_profiles CASCADE CONSTRAINTS;

-- 음식 기본 정보 테이블
CREATE TABLE hk_food_types (
    food_num NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    food_name VARCHAR2(100) NOT NULL,
    category VARCHAR2(50) DEFAULT '기타',  -- '곡류', '채소류', '과일류', '육류', '유제품' 등
    calories_per_100g NUMBER(5,1) NOT NULL,     -- 소수점 1자리
    protein_per_100g NUMBER(5,1) DEFAULT 0,
    carbs_per_100g NUMBER(5,1) DEFAULT 0,
    fat_per_100g NUMBER(5,1) DEFAULT 0,
    is_active CHAR(1) DEFAULT 'Y',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 음식 참고 중량 정보 테이블
CREATE TABLE hk_food_reference_weights (
    reference_num NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    food_num NUMBER NOT NULL,
    reference_unit VARCHAR2(50) NOT NULL,       -- "1공기", "중간크기 1개", "1컵" 등
    weight_grams NUMBER(6,1) NOT NULL,          -- 소수점 1자리
    description VARCHAR2(200),                  -- 추가 설명
    FOREIGN KEY (food_num) REFERENCES hk_food_types(food_num) ON DELETE CASCADE
);

-- 사용자 신체정보 테이블 (선택적)
CREATE TABLE hk_user_profiles (
    user_num NUMBER PRIMARY KEY,
    gender VARCHAR2(10),                        -- 'M', 'F'
    age NUMBER(3),
    weight_kg NUMBER(5,1),                      -- 소수점 1자리
    height_cm NUMBER(5,1),                      -- 키 정보 추가
    target_calories NUMBER(4,0),                -- 목표 칼로리 (정수)
    activity_level VARCHAR2(20) DEFAULT 'MODERATE',  -- 'LOW', 'MODERATE', 'HIGH'
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_num) REFERENCES hk_users(user_num) ON DELETE CASCADE
);

-- 즐겨찾기 음식 테이블
CREATE TABLE hk_user_favorite_foods (
    user_num NUMBER,
    food_num NUMBER,
    added_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (user_num, food_num),
    FOREIGN KEY (user_num) REFERENCES hk_users(user_num) ON DELETE CASCADE,
    FOREIGN KEY (food_num) REFERENCES hk_food_types(food_num) ON DELETE CASCADE
);

-- 사용자 음식 섭취 기록 테이블
CREATE TABLE hk_user_foods (
    food_log_num NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    user_num NUMBER NOT NULL,
    food_num NUMBER NOT NULL,
    amount_grams NUMBER(8,1) NOT NULL,          -- 섭취량 (g, 소수점 1자리)
    calories_consumed NUMBER(6,0) NOT NULL,     -- 섭취 칼로리 (반올림된 정수)
    protein_consumed NUMBER(6,1),               -- 섭취 단백질 (g)
    carbs_consumed NUMBER(6,1),                 -- 섭취 탄수화물 (g)
    fat_consumed NUMBER(6,1),                   -- 섭취 지방 (g)
    meal_type VARCHAR2(20) DEFAULT '기타',       -- '아침', '점심', '저녁', '간식', '기타'
    record_date DATE DEFAULT SYSDATE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (user_num) REFERENCES hk_users(user_num) ON DELETE CASCADE,
    FOREIGN KEY (food_num) REFERENCES hk_food_types(food_num) ON DELETE CASCADE
);

-- 인덱스 생성 (성능 최적화)
CREATE INDEX idx_user_foods_date ON hk_user_foods(user_num, record_date);
CREATE INDEX idx_user_foods_meal ON hk_user_foods(user_num, meal_type, record_date);
CREATE INDEX idx_food_types_name ON hk_food_types(food_name);
CREATE INDEX idx_food_types_category ON hk_food_types(category, is_active);

