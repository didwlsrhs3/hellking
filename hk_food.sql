-- 데이터 확인 쿼리
SELECT 
    f.food_name,
    f.category,
    f.calories_per_100g,
    r.reference_unit,
    r.weight_grams,
    ROUND((f.calories_per_100g * r.weight_grams / 100)) AS calories_per_unit
FROM hk_food_types f
LEFT JOIN hk_food_reference_weights r ON f.food_num = r.food_num
ORDER BY f.category, f.food_name, r.weight_grams;

SELECT food_log_num, food_num, meal_type, record_date, 
       TO_CHAR(record_date, 'YYYY-MM-DD HH24:MI:SS') as formatted_date,
       created_at
FROM hk_user_foods 
WHERE user_num = 1 
ORDER BY created_at DESC;

